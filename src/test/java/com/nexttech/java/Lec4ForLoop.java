package com.nexttech.java;

public class Lec4ForLoop {

	public static void main(String[] args) {
		// print from 0 to 14
		
		for (int y=0;y<=14; y+=2) {
			System.out.println(y);
		}
		
		
		// 2 to 11
		
		for(int e=2; e<=11; e++) {
			System.out.println(e);
		}
		
		// how to write a code all the even numbers
		
		// print all the even numbers from 0 to 50
		
	System.out.println("helllllo");
	
	for(int p=0;p<35; p+=2) {
		System.out.println(p);
	}
	
	for (int j=3;j<=25; j+=3) {
		System.out.println(j);
	}
	
	// print all the odd numbers up to 25
	
	for (int h=0; h<=26; h++) {
		System.out.println(h);
	}
	for (int d=1;d<=25; d=d+5) {
		System.out.println(d);
	}
	
	for (int x=1; x<25; x=x+2) {
		System.out.println(x);
	}
	}
	
	

}
